package com.thanhtu.myAPI.model;

public class JsonUpperCaseToCamelRequestModel {
	private String uppercaseJsonString;
	private String jsonString;
	public String getUppercaseJsonString() {
		return uppercaseJsonString;
	}
	public void setUppercaseJsonString(String uppercaseJsonString) {
		this.uppercaseJsonString = uppercaseJsonString;
	}
	public String getJsonString() {
		return jsonString;
	}
	public void setJsonString(String jsonString) {
		this.jsonString = jsonString;
	}
	
}
